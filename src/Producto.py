class Producto:

    def __init__(self, precio, cantidad, nombre, marca, presentacion, cantidad_stock, requiere_refrigeracion, fecha_elaboracion, fecha_caducidad):

        self.precio = precio
        self.cantidad = cantidad
        self.nombre = nombre
        self.marca = marca
        self.presentacion = presentacion
        self.cantidad_stock = cantidad_stock
        self.requiere_refrigeracion = requiere_refrigeracion
        self.fecha_elaboracion = fecha_elaboracion
        self.fecha_caducidad = fecha_caducidad